


function tinhToan(phepTinh) {
    let i1 = parseInt(document.getElementById('I1').value);
    let i2 = parseInt(document.getElementById('I2').value);

    if (phepTinh == '+') {
        alert(i1 + i2);
    } else if (phepTinh == '-') {
        alert(i1 - i2);
    } else if (phepTinh == '*') {
        alert(i1 * i2);
    } else
        alert(i1 / i2);
    };





